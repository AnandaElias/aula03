package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        double resultado;

        resultado = 15 + 7;
        System.out.println(resultado);

        resultado = 15 - 7;
        System.out.println(resultado);

        resultado = 15 * 7;
        System.out.println(resultado);

        resultado = 15 / 7; // init / int = int ==> resultado será int
        System.out.println(resultado);

        resultado = 15.0 / 7; // double / int = double ==> resultado será double
        System.out.println(resultado); 
        
    }    
}

